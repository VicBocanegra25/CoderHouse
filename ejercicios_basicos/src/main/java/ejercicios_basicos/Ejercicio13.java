package ejercicios_basicos;

// Escribe un programa que imprima los nÃºmeros del 10 al 1 en orden descendente utilizando un bucle for.


public class Ejercicio13 {
	public static void main(String[] args) {
		// Un ciclo for, iniciamos en 10, terminamos en 1 y decrementamos la variable
		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}
	}
}
