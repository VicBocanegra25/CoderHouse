package ejercicios_basicos;

/* 
 * Declara una variable de tipo String y asigna un nombre a esa variable. Luego imprime el contenido de la variable.
 */

public class Ejercicio6 {
	public static void main(String[] args) {
		String miNombre = "Víctor Eduardo";
		String miApellido = "Bocanegra Eugenio";
		String miPais = "México";
		String miCiudad = "Ciudad de México";
		
		// Se imprime un mensaje utilizando las variables. Es necesario agregar espacios entre algunas variables.
		System.out.println("Mi nombre es: " + miNombre + " " + miApellido + ". Vivo en: " + 
		miCiudad + ", " + miPais);
		
	}

}
